                <h6>- Institut Teknologi bandung</h6>
                <p>Hoogeschool te Bandung (TH). Tujuan didirikannya kampus ini untuk memenuhi kebutuhan tenaga teknik pasca Perang Dunia I. Enam tahun kemudian, TH menghasilkan insinyur pertama di Indonesia, yaitu Ir. Soekarno. Beliau lulus dari jurusan Teknik Sipil.
Saat Jepang menjajah Indonesia, TH berganti nama menjadi Sekolah Tinggi Teknik (STT) di Yogyakarta dan bergabung sebagai bagian dari UGM. Kemudian, di tahun 1950-1959 STT berubah menjadi Fakultas Teknik serta Fakultas Ilmu Pasti dan Ilmu Alam UI. Setelah melalui perjalanan panjang, pemerintah Indonesia meresmikan Institut Teknologi Bandung pada tanggal 2 Maret 1959. Kampus ITB Bandung dikenal juga sebagai Kampus Ganesha.

Fasilitas di ITB juga lengkap banget! Ada perpustakaan yang dapat diakses secara offline maupun online, ruang kelas ber AC, laboratorium, WiFi, layanan konseling mahasiswa, kantin, tempat ibadah, koperasi, toko buku, klinik umum, bus antar kampus, serta sarana olahraga dengan kolam renang dan lapangan.</p>    
                <br>
                <h6>- Universitas Indonesia</h6>
                <p>UI adalah salah satu universitas riset atau institusi akademik terkemuka di dunia yang terus mengejar pencapaian tertinggi dalam hal penemuan, pengembangan dan difusi pengetahuan secara regional dan global. 
Dengan prestasi yang terus diraihnya UI berada di peringkat kampus terbaik di Indonesia berdasarkan penilaian Lembaga pemeringkatan dunia.

Sejarah Universitas Indonesia dimulai sejak 1849 dimana UI menjadi representasi institusi pendidikan dengan sejarah tertua di Asia. Hingga hari ini, UI terus memainkan peran penting di tingkat nasional dan global. Memiliki 17 Fakultas dengan lulusan yang kompeten,  UI mengemban misi menjadi lembaga pendidikan yang unggul dan kualitas berstandar dunia</p>
                <br>
                <h6>- Universitas Padjajaran</h6>
                <p>Nasional Perguruan Tinggi pada 16 Januari 2016. Kemudian pada 19 Desember 2018 Unpad kembali meraih akreditasi “A” yang berlaku hingga 19 Desember 2023 berdasarkan SK BAN PT Nomor 408/SK/BAN-PT/Akred/PT/XII/2018.

Universitas Padjadjaran mempunyai dua kampus utama serta dua kampus untuk Program Studi di luar Kampus Utama (PSDKU). Kampus utama Unpad adalah Kampus Jatinangor, Kabupaten Sumedang dan Kampus Iwa Koesoemasoemantri di Dipati Ukur, Bandung. Sedangkan kampus Program Studi di luar Kampus Utama atau Kampus PSDKU berada di Pangandaran dan Garut.</p>
                <br>
                <h6>- Universitas Brawijaya</h6>
                <p>Berdasarkan Keputusan BAN-PT No. 328/SK/BAN-PT/Akred/PT/XII/2018, Universitas Brawijaya (UB) berhasil mempertahankan nilai akreditasi A . Dengan demikian, UB dinyatakan terakreditasi dengan peringkat A selama lima tahun kedepan, sejak 4 Desember 2018 hingga 4 Desember 2023.

2022 Universitas Brawijaya (UB) mendapatkan peringkat ke-3 dalam daftar universitas terbaik di Indonesia versi Webometrics World University Ranking. Universitas Brawijaya juga menempati peringkat 239 dalam daftar kampus terbaik Asia pada tahun 2022 versi QS Asian University Rankings dan masuk dalam kategori 10 besar kampus terbaik di Indonesia versi Kementerian Riset.</p>
                <br>
                <h6>- Universitas Gadjah Mada</h6>
                <p>Rektor Universitas Gadjah Mada memberikan penghargaan kepada mahasiswa peserta program unggulan atau program flagship Kampus Merdeka yang diselenggarakan Kementerian Pendidikan, Kebudayaan, Riset, dan Teknologi (Kemendikbudristek).

UGM memiliki 18 fakultas dan 2 sekolah yang menyelenggarakan program sarjana, pascasarjana, profesi, spesialis, diploma, serta program internasional
Sebagai Universitas Riset Berkelas Dunia, Universitas Gadjah Mada memberikan perhatian khusus terhadap kegiatan-kegiatan penelitian serta mendorong dosen dan mahasiswa untuk melakukan dan mengembangkan berbagai penelitian yang berorientasi untuk memenuhi kebutuhan bangsa.

UGM secara konsisten dan sistematik sejak tahun pendiriannya 1949 berjuang bersama rakyat hingga akar rumput membangun dan menyejahterakan Indonesia. UGM terus mendorong peningkatan kuantitas dan kualitas pengabdian kepada masyarakat berbasis riset di Indonesia maupun di dunia Internasional dengan prinsip-prinsip pembangunan berkelanjutan. UGM secara bertahap membangun kepemimpinan pengabdian kepada masyarakat di dunia internasional, antara lain untuk mendesiminasikan nilai-nilai baik keindonesiaan dan kemanusiaan.</p>
